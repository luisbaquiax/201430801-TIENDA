    public void llenarTablaTiendas() {
        dfm = (DefaultTableModel) tableListaEmpleados.getModel();
        for (Tienda tienda : this.sistema.getTiendas()) {

            String[] datos = {tienda.getNombreTienda(),
                tienda.getDireccion(),
                tienda.getCodigo(),
                tienda.getTelefono(),
                tienda.getTelefono2(),
                tienda.getCorreoElectronico(),
                tienda.getHorario()
            };
            dfm.addRow(datos);
        }
    }

