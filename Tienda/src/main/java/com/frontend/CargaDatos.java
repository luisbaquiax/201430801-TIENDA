    private void llenarTablas() {

        model = (DefaultTableModel) tableTienda.getModel();

        for (Tienda tienda : sistema.getTiendas()) {
            String[] datos = new String[]{
                tienda.getNombreTienda(),
                tienda.getCodigo(),
                tienda.getDireccion(),
                tienda.getTelefono(),
                tienda.getTelefono2()
            };
            model.addRow(datos);
        }

        mode1 = (DefaultTableModel) tabelEnvio.getModel();

        for (TiempoDeEnvio envio : sistema.getTiemposDeEnvio()) {
            String[] datos = new String[]{
                envio.getCodigoTIENDA1(),
                envio.getCodigoTIENDA2(),
                envio.getTiempoEnvio() + ""
            };
            mode1.addRow(datos);
        }
        mode2 = (DefaultTableModel) tableCustomers.getModel();
        for (Cliente c : sistema.getClientes()) {
            String[] datos = new String[]{
                c.getNombreCliente(),
                c.getNit(),
                c.getTelefono() + "",
                c.getCredito() + ""
            };
            mode2.addRow(datos);
        }
        mode3 = (DefaultTableModel) tableEmployees.getModel();
        for (Empleado e : sistema.getEmpleados()) {
            String[] datos = new String[]{
                e.getNomgre(),
                e.getCodigo(),
                e.getTelefono() + "",
                e.getDpi() + ""
            };
            mode3.addRow(datos);
        }
        mode4 = (DefaultTableModel) tableErrores.getModel();
        for (String e : sistema.getDatosErroneos()) {
            String[] datos = new String[]{
                e
            };
            mode4.addRow(datos);
        }
        mode5 = (DefaultTableModel) tablePedidos.getModel();
        for (Pedido e : sistema.getPedidos()) {
            String[] datos = new String[]{
                e.getCodigoProductoPedido(),
                e.getCodigoTiendaORIGEN(),
                e.getCodigoTiendaDESTINO(),
                e.getFechaPedido().mostrarFECHAS(),
                e.getNitCliente(),
                e.getCodigoProductoPedido() + "",
                e.getCantidadArticulos() + "",
                e.getTotalPagar() + "",
                e.getAnticipo() + ""

            };
            mode5.addRow(datos);
        }
        mode6 = (DefaultTableModel) tableProducts.getModel();
        for (Producto e : sistema.getProductos()) {
            String[] datos = new String[]{
                e.getNombre(),
                e.getFabricante(),
                e.getCodigo(),
                e.getCantidad() + "",
                e.getPrecio() + "",
                e.getTiendaDondeExiste() + ""
            };
            mode6.addRow(datos);
        }
    }

